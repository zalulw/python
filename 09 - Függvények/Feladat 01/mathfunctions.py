def sumOfTwo(a: float, b: float)->float:
    result:float = None
    result = a + b

    return result

def multipleTwo(a: float, b: float)->float:
    result:float = None
    result = a * b

    return result

def divideTwo(a: float, b:float)->float:
    result:float = None
    result = a / b

    return result

def substractTwo(a: float, b:float)->float:
    result:float = None
    result = a - b